#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i)
	{
		if (s[i] == (char)c)
			return (&s[i]);
		i--;
	}
	if (s[0] == (char)c)
		return (s);
	return (0);
}
